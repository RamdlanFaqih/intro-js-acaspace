// Arithmethic Operator

let a = 10;
let b = 3;

// Penjumlahan
console.log(a + b); // 13

// Pengurangan
console.log(a - b); // 7

// Perkalian
console.log(a * b); // 30

// Pembagian
console.log(a / b); // 3.333...

// Modulus (sisa bagi)
console.log(a % b); // 1

// Eksponen (pangkat)
console.log(a ** b); // 1000

// Increment
a++;
console.log(a); // 11

// Decrement
b--;
console.log(b); // 2


// Comparison Operator
let passwordDaftar = "rahasia";
let passwordIsi = "rahasia";

// apakah passwordDaftar sama dengan passwordIsi?
// output: true
console.log(passwordDaftar === passwordIsi);

// apakah passwordDaftar tidak sama dengan passwordIsi?
// output: false
console.log(passwordDaftar !== passwordIsi);

let umurNaekMotor = 17;
let udin = 18;

// apakah udin lebih dari umurNaekMotor?
console.log(udin > umurNaekMotor); // true

// apakah udin kurang dari umurNaekMotor?
console.log(udin < umurNaekMotor); // false

// apakah udin lebih dari atau sama dengan umurNaekMotor?
console.log(udin >= umurNaekMotor); // true

// apakah udin kurang dari atau sama dengan umurNaekMotor?
console.log(udin <= umurNaekMotor); // false
